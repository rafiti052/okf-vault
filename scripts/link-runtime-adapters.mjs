#!/usr/bin/env node
/**
 * Recreate runtime adapter symlinks when Git checks out symlink paths as plain files
 * (common on Windows with core.symlinks=false). Idempotent — skips existing valid links.
 *
 * Emits, per project:
 * - Umbrella skill symlinks (`/okf-vault` auto-applies):
 *   `.cursor/skills/okf-vault` and `.claude/skills/okf-vault` → canonical skill.
 * - Per-command discoverable units so every `/okv-*` shows up individually:
 *   Cursor `.cursor/skills/<cmd>/SKILL.md` and Claude `.claude/commands/<cmd>.md` → canonical `commands/<cmd>.md`.
 */
import { existsSync, lstatSync, mkdirSync, realpathSync, rmSync, symlinkSync } from "node:fs";
import { basename, dirname, join, relative, resolve, isAbsolute } from "node:path";
import { fileURLToPath } from "node:url";
import { listLegacyArtifacts, listManagedArtifacts, OKV_COMMANDS } from "./managed-artifacts.mjs";

export { OKV_COMMANDS };

const defaultRoot = join(dirname(fileURLToPath(import.meta.url)), "..");

/**
 * @param {string} path
 * @returns {boolean}
 */
function pathExistsOrSymlink(path) {
  try {
    lstatSync(path);
    return true;
  } catch {
    return false;
  }
}

/**
 * True when `candidate` is the same path as, or nested under, `root`.
 * @param {string} root
 * @param {string} candidate
 * @returns {boolean}
 */
function isInsideRoot(root, candidate) {
  const rel = relative(root, candidate);
  return rel === "" || (!rel.startsWith("..") && !isAbsolute(rel));
}

/**
 * Resolves a symlink to its real target without throwing on broken links.
 * @param {string} linkPath
 * @returns {string | null}
 */
function safeRealpath(linkPath) {
  try {
    return realpathSync(linkPath);
  } catch {
    return null;
  }
}

/**
 * @param {string} linkPath
 * @param {string} canonicalPath absolute path the link must resolve to
 * @returns {boolean}
 */
function isValidSymlink(linkPath, canonicalPath) {
  if (!existsSync(linkPath) || !lstatSync(linkPath).isSymbolicLink()) {
    return false;
  }
  const resolvedLink = safeRealpath(linkPath);
  const resolvedCanonical = safeRealpath(canonicalPath);
  return resolvedLink !== null && resolvedCanonical !== null && resolvedLink === resolvedCanonical;
}

/**
 * Computes the symlink target string for a link pointing at a canonical path.
 * Uses a relative target from the link's own directory when the canonical target stays inside the
 * project root; falls back to an absolute path when it escapes (foreign-repo `okf-vault init`).
 * @param {string} linkPath
 * @param {string} canonicalPath
 * @param {string} projectRoot
 * @returns {string}
 */
function computeSymlinkTarget(linkPath, canonicalPath, projectRoot) {
  const resolvedProject = resolve(projectRoot);
  const resolvedCanonical = resolve(canonicalPath);
  if (!isInsideRoot(resolvedProject, resolvedCanonical)) {
    return resolvedCanonical;
  }
  return relative(dirname(resolve(linkPath)), resolvedCanonical);
}

/**
 * Idempotently (re)creates one symlink at `linkPath` resolving to `canonicalPath`.
 * Handles both directory links (umbrella skills) and file links (`SKILL.md`, command files).
 * @param {{ linkPath: string; canonicalPath: string; label: string; projectRoot: string; quiet: boolean; linked: string[]; skipped: string[] }} params
 */
export function ensureSymlink({
  linkPath,
  canonicalPath,
  label,
  projectRoot,
  quiet,
  linked,
  skipped,
}) {
  mkdirSync(dirname(linkPath), { recursive: true });
  if (isValidSymlink(linkPath, canonicalPath)) {
    if (!quiet) {
      console.log(`ok: ${label} symlink already valid at ${linkPath}`);
    }
    skipped.push(linkPath);
    return;
  }
  rmSync(linkPath, { recursive: true, force: true });
  const target = computeSymlinkTarget(linkPath, canonicalPath, projectRoot);
  symlinkSync(target, linkPath);
  if (!quiet) {
    console.log(`linked: ${label} → ${target}`);
  }
  linked.push(linkPath);
}

/**
 * Removes legacy pre-OKV artifacts generated by older setup/init runs.
 * Broken symlinks are removed through lstat-based existence checks.
 * @param {string} projectRoot
 * @param {{ verbose?: boolean }} [options]
 * @returns {{ removed: string[] }}
 */
export function sweepLegacyArtifacts(projectRoot, options = {}) {
  const resolvedProject = resolve(projectRoot);
  const verbose = options.verbose === true || process.env.OKV_VERBOSE === "1";
  const removed = [];

  for (const artifact of listLegacyArtifacts(resolvedProject)) {
    if (artifact.path === undefined || !pathExistsOrSymlink(artifact.path)) {
      continue;
    }
    rmSync(artifact.path, { recursive: true, force: true });
    if (
      basename(artifact.path) === "SKILL.md" &&
      basename(dirname(artifact.path)).startsWith("vault-")
    ) {
      rmSync(dirname(artifact.path), { recursive: true, force: true });
    }
    removed.push(artifact.path);
    if (verbose) {
      console.error(`removed legacy artifact: ${artifact.path}`);
    }
  }

  return { removed };
}

/**
 * @param {string} artifactPath
 * @returns {string | null}
 */
function commandFromArtifactPath(artifactPath) {
  for (const command of OKV_COMMANDS) {
    if (
      artifactPath.endsWith(join(".cursor", "skills", command, "SKILL.md")) ||
      artifactPath.endsWith(join(".claude", "commands", `${command}.md`))
    ) {
      return command;
    }
  }
  return null;
}

/**
 * @param {{ path?: string; target?: string; label: string }} artifact
 * @param {string} canonicalSkillRoot
 * @returns {{ linkPath: string; canonicalPath: string; label: string } | null}
 */
function linkFromManagedArtifact(artifact, canonicalSkillRoot) {
  if (artifact.path === undefined || artifact.target === undefined) {
    return null;
  }
  const command = commandFromArtifactPath(artifact.path);
  const canonicalPath =
    command === null
      ? resolve(canonicalSkillRoot)
      : join(resolve(canonicalSkillRoot), "commands", `${command}.md`);
  return {
    linkPath: artifact.path,
    canonicalPath,
    label: artifact.label,
  };
}

/**
 * @param {{ projectRoot: string; canonicalSkillRoot: string; quiet?: boolean }} options
 * @returns {{ linked: string[]; skipped: string[] }}
 */
export function linkRuntimeAdapters({
  projectRoot,
  canonicalSkillRoot,
  quiet = false,
  sweep = true,
}) {
  const resolvedProject = resolve(projectRoot);
  const resolvedCanonical = resolve(canonicalSkillRoot);
  const { removed } = sweep
    ? sweepLegacyArtifacts(resolvedProject)
    : {
        removed: [],
      };
  const linked = [];
  const skipped = [];

  const links = listManagedArtifacts(resolvedProject)
    .filter((artifact) => artifact.kind === "symlink")
    .map((artifact) => linkFromManagedArtifact(artifact, resolvedCanonical))
    .filter((artifact) => artifact !== null);

  for (const { linkPath, canonicalPath, label } of links) {
    ensureSymlink({
      linkPath,
      canonicalPath,
      label,
      projectRoot: resolvedProject,
      quiet,
      linked,
      skipped,
    });
  }

  if (!quiet) {
    if (removed.length > 0) {
      console.log(`Removed ${removed.length} legacy runtime artifact(s).`);
    }
    console.log(
      `Runtime adapter symlinks ready (${linked.length} linked, ${skipped.length} skipped).`,
    );
  }

  return { linked, skipped, removed };
}

function parseCliArgs(argv) {
  let projectRoot = defaultRoot;
  let canonicalSkillRoot = join(defaultRoot, ".agents", "skills", "okf-vault");
  let json = false;
  let quiet = false;

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--json") {
      json = true;
      quiet = true;
      continue;
    }
    if (arg === "--quiet") {
      quiet = true;
      continue;
    }
    if (arg === "--project-root") {
      const value = argv[index + 1];
      if (value === undefined) {
        throw new Error("Missing value for --project-root");
      }
      projectRoot = value;
      index += 1;
      continue;
    }
    if (arg === "--canonical-skill-root") {
      const value = argv[index + 1];
      if (value === undefined) {
        throw new Error("Missing value for --canonical-skill-root");
      }
      canonicalSkillRoot = value;
      index += 1;
      continue;
    }
    throw new Error(`Unknown argument: ${arg}`);
  }

  return { projectRoot, canonicalSkillRoot, json, quiet };
}

const isDirectRun =
  process.argv[1] !== undefined && fileURLToPath(import.meta.url) === resolve(process.argv[1]);

if (isDirectRun) {
  try {
    const { projectRoot, canonicalSkillRoot, json, quiet } = parseCliArgs(process.argv.slice(2));
    const data = linkRuntimeAdapters({ projectRoot, canonicalSkillRoot, quiet });
    if (json) {
      process.stdout.write(
        `${JSON.stringify({ status: "ok", command: "link-runtime-adapters", data })}\n`,
      );
    }
  } catch (error) {
    const message = error instanceof Error ? error.message : String(error);
    console.error(`error: ${message}`);
    console.error(
      "Usage: node scripts/link-runtime-adapters.mjs [--json] [--quiet] [--project-root <path>] [--canonical-skill-root <path>]",
    );
    process.exit(1);
  }
}
